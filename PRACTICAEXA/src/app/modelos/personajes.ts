export class Personajes {

    nombre : string;
    caracteristica1 : string;
    caracteristica2 : string;
    caracteristica3 : string;
    detalles : string;

    constructor(nombre : string, caracteristica1 : string, caracteristica2 : string, caracteristica3 : string, detalles : string){
        this.nombre = nombre;
        this.caracteristica1 = caracteristica1;
        this.caracteristica2 = caracteristica2;
        this.caracteristica3 = caracteristica3;
        this.detalles = detalles;
    }
}
